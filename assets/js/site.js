/**
 * Traduire la page
 */
function translatePage(lang) {
  console.log(`üîÑ translatePage appel√© avec: ${lang}`);
  
  const translations = siteTranslations[lang] || siteTranslations.fr;
  console.log(`üìö Traductions trouv√©es pour ${lang}:`, !!siteTranslations[lang]);
  
  // Traduire par data-translate
  const elementsToTranslate = document.querySelectorAll('[data-translate]');
  console.log(`üè∑Ô∏è ${elementsToTranslate.length} √©l√©ments avec data-translate trouv√©s`);
  
  let translatedCount = 0;
  elementsToTranslate.forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[key]) {
      const oldText = element.textContent;
      element.textContent = translations[key];
      translatedCount++;
      console.log(`üîÑ ${key}: "${oldText}" ‚Üí "${translations[key]}"`);
    } else {
      console.warn(`‚ö†Ô∏è Cl√© manquante: ${key} pour la langue ${lang}`);
    }
  });
  
  console.log(`‚úÖ ${translatedCount} √©l√©ments traduits en ${lang.toUpperCase()}`);
  
  // Traduire les placeholders
  const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
  console.log(`üìù ${placeholderElements.length} placeholders trouv√©s`);
  
  let placeholderCount = 0;
  placeholderElements.forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    if (translations[key]) {
      element.placeholder = translations[key];
      placeholderCount++;
      console.log(`üîÑ Placeholder ${key}: ‚Üí "${translations[key]}"`);
    } else {
      console.warn(`‚ö†Ô∏è Cl√© placeholder manquante: ${key} pour la langue ${lang}`);
    }
  });
  
  console.log(`‚úÖ ${placeholderCount} placeholders traduits`);
  
  // Notify game that language has changed (if game is loaded in another tab/frame)
  try {
    if (window.parent && window.parent !== window) {
      window.parent.postMessage({ type: 'languageChange', language: lang }, '*');
    }
  } catch (e) {
    // Ignore cross-origin errors
  }
}

